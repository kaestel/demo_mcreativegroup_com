					<h2>The making of M</h2>
					<p>15 years ago we started a video production company called Millennium Creative Group. Today, we have evolved into M, a full-service creative agency dedicated to making and moving media content. We’d be lying if we told you this process of reevaluation, redefinition, and repositioning has been easy. It hasn’t. It’s taken a ton of reflection, days of discussions, hour upon hour of analysis and self-examination. Who are we? Who do we want to be? What do we stand for? What do we bring to the game that sets us apart? What do we believe in?</p>

					<p>The answers to those questions lie before you now in our new website. It embodies our conviction that simplicity, clarity, and clean design are the most powerful tools in marketing today. It speaks to our belief that flash and hyperbole have no place in a media landscape characterized by clutter and constant noise. In the sum of its parts, it is a testament to the idea that the best way a business can send a message is to <span class="italicbold">be</span> the message.</p>

					<p>While it’s a work in progress and will be sharpened, polished and perfected in the days and months ahead, it’s a solid start and showcases our passion for partnering 
					with brands and businesses to tell stories that inform, entertain, and inspire.</p>
