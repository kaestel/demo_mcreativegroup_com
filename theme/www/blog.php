					<h2>Who moved us in 2011</h2>
					<p>From the launch of a product to the introduction of a service solution, so much of the <a href="/work">media we make at M</a> is about what’s new, what’s now, and what’s next. At the start of our 16th planetary orbit, it seemed fitting to us to mark the passage of time with a little look back at the year we left behind. Here are five personalities we think made the year especially memorable.</p>

					<p><span class="italic">The</span> <a href="http://www.wired.com/epicenter/2011/10/jobs/all/1" target="_blank">global icon for innovation</a>, <span class="bold">Steve Jobs</span> left the realm this year. In his passing, he’s become a global brand in his own right, more widely known and admired now than ever before.</p>

					<p>On September 22, 2011 <span class="bold">Meg Whitman</span> took over as the <a href="http://en.wikipedia.org/wiki/Meg_Whitman" target="_blank">new president and CEO of HP</a>. Do you ever wonder what it must be like to pilot a 120 billion dollar business? We do.</p>

					<p> <span class="bold">Wilmot Reed Hastings</span> isn’t quite a household name yet but we expect him to be before long. The co-founder of Netflix, Hastings and the company he made continue to challenge the media conglomerate status quo. Not bad for a guy named <a href="http://en.wikipedia.org/wiki/Reed_Hastings" target="_blank">Wilmot</a>.</p>

					<p><span class="bold">Seth Godin</span> continues to be as prolific as ever. He published two books in ’11, <a href="http://www.youtube.com/watch?v=6IapqgokfNU" class="italic" target="_blank">Poke The Box</a>, and <a href="http://sethgodin.typepad.com/seths_blog/2011/09/we-are-all-weird.html" class="italic" target="_blank">We Are All Weird</a>. We like the way Seth thinks and writes so we definitely recommend his books and his blog. Interesting that the word God is in his last name.</p>

					<p><span class="bold">Brian Griffin</span> is a truly remarkable dog. Not only can he walk (upright) and talk (intelligently), in 2011 he went toe-to-toe with Arianna Huffington on <a href="http://www.youtube.com/watch?v=BNbpSv4-4Is" class="italic" target="_blank">Real Time with Bill Maher</a>. What can your dog do?</p>
