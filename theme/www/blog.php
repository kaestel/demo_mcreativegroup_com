					<h2>The C Word</h2>
					<p>So why is everybody talking about the cloud like it’s the second coming of the Internet? Well, because it is.</p>

<p>Just to clarify, <span class="bold">cloud computing</span> is the delivery of computing as a service rather than a product. So you get all kinds of computing functionality, most commonly delivered through application program interfaces (APIs), without having to buy hardware and/or software capable of performing all those functions. The actual term <a href="http://www.youtube.com/watch?v=QJncFirhjPg" target="_blank">“cloud”</a> is just a metaphor for the Internet, really just a word marketers have used to make the notion sound sexy.</p>

						<p>Speaking of sexy, Google, facebook, Amazon, and Apple lead the way in cloud computing services with infrastructures that are actually used by other companies to offer their own cloud services. With more and more companies packaging the so-called cloud, your Internet connection is a ticket to all kinds of computing power you didn’t have before the cloud came along. In the wise, wonderful words of a woman at an airport in a <a href="http://www.youtube.com/watch?v=Lel3swo4RMc" target="_blank">Windows 7 ad</a>, “Yay cloud.”</p>
